import sys
input=sys.stdin.readline
MOD=1000000009

def main():
    t=int(input())
    dp=[0]*1000001
    
    dp[1]=1
    dp[2]=2
    dp[3]=4
    for i in range(4,1000001):
            dp[i]=dp[i-3]%MOD+dp[i-2]%MOD+dp[i-1]%MOD
    
    for _ in range(t):
        n=int(input())
        print(dp[n]%MOD)

main()

